  function sortbyprice()
{
    var selected = document.querySelector("#sortbyprice").value;
    if(selected=="low")
    {
      Accessorydata.sort(function(a,b)
        {
            return a.price - b.price
        })
    }
    if(selected=="high")
    {
      Accessorydata.sort(function(a,b)
        {
            return b.price - a.price
        })
    }
    displaydata(Accessorydata)
 
}

